#include <stdio.h>
int main()
{
	int arg1 = 10;
	int arg2 = 20;
	printf("****Arthimatic operations****\n");
	printf("(arg1+arg2): %d\n", (arg1 + arg2));
	printf("(arg1-arg2): %d\n", (arg1 - arg2));
	printf("(arg1*arg2): %d\n", (arg1 * arg2));
	printf("(arg1/arg2): %f\n", ((float)arg1 / arg2));
	printf("(arg1%arg2): %d\n", (arg1 % arg2));
	return 0;
}