#include <stdio.h>
#include <math.h>
int main()
{
    int arg1 = 10;
    int arg2 = 20;
    printf("(arg1 > arg2)? : %d\n", (arg1 > arg2));
    printf("(arg1 >= arg2)? : %d\n", (arg1 >= arg2));
    printf("(arg1 < arg2)? : %d\n", (arg1 < arg2));
    printf("(arg1 <= arg2)? : %d\n", (arg1 <= arg2));
    printf("(arg1 == arg2)? : %d\n", (arg1 == arg2));
    printf("(arg1 != arg2)? : %d\n", (arg1 != arg2));
    return 0;
}